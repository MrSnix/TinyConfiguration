package org.tinyconfiguration.abc.data;

/**
 * {@link Datatype} is an enumeration used to track object type and avoid useless type checking
 */
public enum Datatype {

    ARR_BOOLEAN, ARR_BYTE,
    ARR_CHAR, ARR_DOUBLE,
    ARR_FLOAT, ARR_INT,
    ARR_LONG, ARR_SHORT,
    ARR_STRING, ARR_UNKNOWN,
    BOOLEAN, BYTE,
    CHAR, DOUBLE,
    FLOAT, INT,
    LONG, SHORT,
    STRING, UNKNOWN


}
